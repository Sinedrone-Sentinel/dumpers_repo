-- Helper: true when the current session user is on the ban list
CREATE OR REPLACE FUNCTION public.is_current_user_banned()
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.banned_users WHERE id = auth.uid()
  );
$$;

-- RESTRICTIVE policies deny access when the caller is banned
CREATE POLICY "profiles_deny_banned"
  ON public.profiles
  AS RESTRICTIVE
  FOR ALL
  TO authenticated
  USING (NOT public.is_current_user_banned());

CREATE POLICY "acquired_blueprints_deny_banned"
  ON public.acquired_blueprints
  AS RESTRICTIVE
  FOR ALL
  TO authenticated
  USING (NOT public.is_current_user_banned());

-- =============================================================================
-- ban_member RPC
-- =============================================================================

CREATE OR REPLACE FUNCTION public.ban_member(
  target_user_id uuid,
  reason text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  caller_role text;
  target_role text;
  profile_row public.profiles%ROWTYPE;
BEGIN
  SELECT role INTO caller_role
  FROM public.profiles
  WHERE id = auth.uid();

  IF caller_role IS NULL OR caller_role NOT IN ('officer', 'super-admin') THEN
    RAISE EXCEPTION 'Permission denied: officer or super-admin required';
  END IF;

  IF target_user_id = auth.uid() THEN
    RAISE EXCEPTION 'Cannot ban yourself';
  END IF;

  SELECT * INTO profile_row
  FROM public.profiles
  WHERE id = target_user_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'User not found';
  END IF;

  target_role := profile_row.role;

  IF target_role = 'super-admin' THEN
    RAISE EXCEPTION 'Cannot ban a super-admin';
  END IF;

  DELETE FROM public.acquired_blueprints
  WHERE user_id = target_user_id;

  INSERT INTO public.banned_users (
    id, email, display_name, rsi_handle, avatar_url,
    banned_at, banned_by, reason
  )
  VALUES (
    profile_row.id,
    profile_row.email,
    profile_row.display_name,
    profile_row.rsi_handle,
    profile_row.avatar_url,
    now(),
    auth.uid(),
    reason
  );

  DELETE FROM public.profiles
  WHERE id = target_user_id;

  RETURN jsonb_build_object('success', true, 'banned_user_id', target_user_id);
END;
$$;

REVOKE ALL ON FUNCTION public.ban_member(uuid, text) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.ban_member(uuid, text) TO authenticated;

-- Super-admin unban: remove from banned_users, restore profile as pending
-- Run in Supabase SQL Editor after 001_banned_users.sql

CREATE OR REPLACE FUNCTION public.unban_member(target_user_id uuid)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  caller_role text;
  ban_row public.banned_users%ROWTYPE;
BEGIN
  SELECT role INTO caller_role
  FROM public.profiles
  WHERE id = auth.uid();

  IF caller_role IS DISTINCT FROM 'super-admin' THEN
    RAISE EXCEPTION 'Permission denied: super-admin required';
  END IF;

  SELECT * INTO ban_row
  FROM public.banned_users
  WHERE id = target_user_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'User is not banned';
  END IF;

  IF EXISTS (SELECT 1 FROM public.profiles WHERE id = target_user_id) THEN
    RAISE EXCEPTION 'Profile already exists for this user';
  END IF;

  DELETE FROM public.banned_users
  WHERE id = target_user_id;

  INSERT INTO public.profiles (
    id, email, display_name, avatar_url, rsi_handle, role
  )
  VALUES (
    ban_row.id,
    ban_row.email,
    ban_row.display_name,
    ban_row.avatar_url,
    ban_row.rsi_handle,
    'pending'
  );

  RETURN jsonb_build_object('success', true, 'unbanned_user_id', target_user_id);
END;
$$;

REVOKE ALL ON FUNCTION public.unban_member(uuid) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.unban_member(uuid) TO authenticated;

-- Self-service account deletion (does not modify banned_users)
-- Run in Supabase SQL Editor

CREATE OR REPLACE FUNCTION public.delete_own_account()
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  caller_id uuid;
  caller_role text;
BEGIN
  caller_id := auth.uid();

  IF caller_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  IF EXISTS (SELECT 1 FROM public.banned_users WHERE id = caller_id) THEN
    RAISE EXCEPTION 'Banned accounts cannot be deleted through settings';
  END IF;

  SELECT role INTO caller_role
  FROM public.profiles
  WHERE id = caller_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Profile not found';
  END IF;

  IF caller_role = 'super-admin' THEN
    RAISE EXCEPTION 'Super-admin accounts cannot self-delete';
  END IF;

  -- Clear references from other profiles (approved_by has no ON DELETE SET NULL)
  UPDATE public.profiles
  SET approved_by = NULL
  WHERE approved_by = caller_id;

  DELETE FROM public.acquired_blueprints
  WHERE user_id = caller_id;

  DELETE FROM public.profiles
  WHERE id = caller_id;

  -- banned_users intentionally untouched (audit/blocklist preserved)

  RETURN jsonb_build_object('success', true, 'deleted_user_id', caller_id);
END;
$$;

REVOKE ALL ON FUNCTION public.delete_own_account() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.delete_own_account() TO authenticated;

