CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  email text,
  display_name text,
  avatar_url text,
  rsi_handle text,
  role text NOT NULL DEFAULT 'pending'
    CHECK (role IN ('pending', 'member', 'officer', 'super-admin')),
  created_at timestamptz NOT NULL DEFAULT now(),
  approved_at timestamptz,
  approved_by uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  ghost_mode boolean NOT NULL DEFAULT false,
  preview_features_enabled boolean NOT NULL DEFAULT false,
  craft_deduct_inventory boolean NOT NULL DEFAULT false
);

CREATE INDEX IF NOT EXISTS profiles_ghost_mode_idx
  ON public.profiles (ghost_mode)
  WHERE ghost_mode = true;

ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "profiles_select_authenticated" ON public.profiles;
CREATE POLICY "profiles_select_authenticated"
  ON public.profiles FOR SELECT TO authenticated
  USING (true);

DROP POLICY IF EXISTS "profiles_update_own" ON public.profiles;
CREATE POLICY "profiles_update_own"
  ON public.profiles FOR UPDATE TO authenticated
  USING (id = auth.uid())
  WITH CHECK (id = auth.uid());

DROP POLICY IF EXISTS "profiles_update_officers" ON public.profiles;
CREATE POLICY "profiles_update_officers"
  ON public.profiles FOR UPDATE TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.profiles p
      WHERE p.id = auth.uid() AND p.role IN ('officer', 'super-admin')
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.profiles p
      WHERE p.id = auth.uid() AND p.role IN ('officer', 'super-admin')
    )
  );

